                Festival Hari Santri 2022 ini merupakan kali kedua yang dilaksanakan oleh Pesantren Modern PRIMAGO. Kegiatan ini hampir sama dengan kegiatan tahun sebelumnya yaitu adanya beberapa perlombaan, layanan bekam gratis serta tabligh akbar, yang membedakan di tanggal 22 oktober ini akan diadakannya Santri PRIMAGO Performances.
                <br>
                <br>
                “Kegiatan ini masih sama dengan tahun sebelumnya yaitu diadakan secara Virtual dengan harapan gaung dari Hari Santri ini bisa terdengar se nasional bahkan internasional,” ujar Ketua Pelaksana Festival Hari Santri 2022 Ustadz Irfan Maulana
                <br>
                <br>
                Berbeda dengan tahun sebelumnya yang diadakan secara gratis, kali ini peserta lomba Festival Hari Santri 2022 dikenai biaya sebesar Rp 25 ribu. Seluruh uang pendaftaran akan disalurkan 100% untuk program Wakaf Pembebesan Lahan Kampus Pesantren Modern PRIMAGO yang bertempat di Kp.Lebak Ela, Desa Cidokom, Kec.Rumpin, Kab. Bogor. 
                <br>
                <br>
                Dengan mengusung tema “Santri Berdaya, Berkarya, Bermanfaat” ustadz Irfan Maulana mengatakan, kegiatan ini akan dimeriahkan oleh berbagai perlombaan seperti, MTQ (Musabaqah Tartil Qur’an), MHQ (Musabahqah Hifzil Qur’an), Adzan khusus putra, membaca puisi islami, mendongeng kisah islami dan cerdas cermat online. Adapula bekam gratis, santri PRIMAGO performance dan diakhiri dengan tabligh akbar virtual.
                <br>
                <br>
                “Hanya lomba cerdas cermat yang memerlukan koneksi internet stabil karena menggunakan aplikasi Zoom dan juga Kahoot. Kalau perlombaan yang lain dilakukan secara virtual, peserta tinggal mengirimkan video berupa link google drive kepada panitia” tutur Irfan Maulana (Rabu 11/10/2022)
                <br>
                <br>
                Selain itu, lanjut Irfan, pihaknya akan mengadakan rentetan acara tabligh akbar dan perlombaan serta Santri PRIMAGO performance selama 7 jam nonstop dan ditayangkan secara live di youtube PRIMAGO TV.
