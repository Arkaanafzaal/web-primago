            <p>Pesantren Modern PRIMAGO merupakan salah satu pesantren persiapan para calon santri untuk masuk Gontor yang berlokasi di Depok selalu turut memeriahkan berbagai peringatan Hari Besar Islam maupun Nasional bagi para calon santri maupun khalayak umum
                <br>
                <br>
